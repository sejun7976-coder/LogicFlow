                ?: return@withContext Result.failure(Exception("평가 결과를 찾을 수 없습니다."))

            val passage = dao.getPassageById(result.passageId)
                ?: return@withContext Result.failure(Exception("지문을 찾을 수 없습니다."))

            // Deserialize history
            val type = object : TypeToken<List<ChatMessage>>() {}.type
            val currentHistory: List<ChatMessage> = try {
                gson.fromJson(result.chatHistoryJson, type) ?: emptyList()
            } catch (e: Exception) {
                emptyList()
            }

            // Append user message
            val newUserMessage = ChatMessage("user", messageText)
            val updatedHistory = currentHistory + newUserMessage

            val apiKey = getApiKey()
            val modelName = resolveModelName(getModelName())

            val botResponseContent: String = if (apiKey.isBlank()) {
                // Mock response
                "지문 '${passage.title}'의 내용을 참고할 때, 제시해주신 의문 '${messageText}'은 핵심 구조 파악에 큰 도움이 됩니다. 다만 데모 모드이므로 구체적인 맞춤형 피드백을 드리기에 제한이 있습니다."
            } else {
                try {
                    // Prepare contents for Gemini request
                    val systemContext = """
                        당신은 사용자의 논리 독해 공부를 돕는 AI 챗봇 튜터입니다.
                        
                        [독해 지문]
                        ${passage.title}
                        ${passage.content}
                        
                        [사용자가 작성한 요약]
                        ${result.userSummary}
                        
                        [AI 평가 피드백]
                        ${result.aiFeedback}
                        
                        위 정보를 바탕으로, 사용자가 질문하는 것에 대해 친절하고 정확하게 논리적으로 설명해주세요. 지문 내용과 사용자가 작성한 요약의 맥락에서 벗어나지 마세요.
                    """.trimIndent()

                    val geminiContents = mutableListOf<Content>()
                    
                    geminiContents.add(Content(role = "user", parts = listOf(Part(systemContext))))
                    geminiContents.add(Content(role = "model", parts = listOf(Part("네, 해당 지문과 피드백 내용을 토대로 도움을 드리겠습니다. 무엇이 궁금하신가요?"))))

                    // Map updatedHistory to Gemini format
                    updatedHistory.forEach { msg ->
                        geminiContents.add(
                            Content(
                                role = if (msg.role == "user") "user" else "model",
                                parts = listOf(Part(msg.content))
                            )
                        )
                    }

                    val request = GeminiRequest(contents = geminiContents)
                    val response = apiService.generateContent(
                        model = modelName,
                        apiKey = apiKey,
                        request = request
                    )

                    response.candidates?.firstOrNull()?.content?.parts?.firstOrNull()?.text
                        ?: "죄송합니다. 답변을 얻지 못했습니다. 다시 시도해 주세요."
                } catch (e: Exception) {
                    "API 통신 오류가 발생했습니다 (${e.message ?: e.javaClass.simpleName}). 잠시 후 다시 시도하거나 설정에서 API Key 상태를 확인해주세요."
                }
            }

            val newBotMessage = ChatMessage("model", botResponseContent)
            val finalHistory = updatedHistory + newBotMessage
            val finalHistoryJson = gson.toJson(finalHistory)

            val updatedResult = result.copy(chatHistoryJson = finalHistoryJson)
            dao.insertAnalysisResult(updatedResult)

            Result.success(updatedResult)
        } catch (e: Exception) {
            Result.failure(e)
        }
    }

    private fun createDemoResult(passage: PassageEntity, userSummary: String): AnalysisResultEntity {
        // Calculate a mock score based on similarity of words
        val words = userSummary.split(" ").filter { it.length > 1 }.toSet()
        val passageWords = passage.content.split(" ").filter { it.length > 1 }.toSet()
        val common = words.intersect(passageWords).size
        val score = if (words.isEmpty()) 50 else (60 + (common.toFloat() / words.size * 40).toInt()).coerceIn(50, 95)

        return AnalysisResultEntity(
            id = "demo_" + System.currentTimeMillis(),
            passageId = passage.id,
            passageTitle = passage.title,
            userSummary = userSummary,
            score = score,
            grade = when {
                score >= 85 -> "높은 신뢰도 등급"
                score >= 70 -> "보통 신뢰도 등급"
                else -> "낮은 신뢰도 등급"
            },
            semanticMatch = score + 5,
            contextPreservation = score - 5,
            premiseCheck = score >= 75,
            premiseDetail = if (score >= 75) "모든 기본 전제가 표준 논리 구조와 일치합니다." else "요약문 일부 전제에 오류나 오해가 있습니다.",
            inferenceCheck = score >= 70,
            inferenceDetail = if (score >= 70) "단계별 추론이 논리적 비약 없이 일관되게 유지됩니다." else "일부 추론 과정에서 비약이 관찰되었습니다.",
            exceptionCheck = score < 85,
            exceptionDetail = if (score >= 85) "예외 상황이나 극단적 논리 모순이 발견되지 않았습니다." else "특정 극단적 상황에서 모순이 발견되었습니다.",
            aiFeedback = "[데모 모드] 사용자의 요약은 지문의 중심 논점을 적절히 포함하고 있습니다. 다만, ${if (score < 80) "핵심 용어의 인과 관계 설명이 다소 미흡하여" else "논리적 흐름의 매끄러움이 보완된다면"} 더욱 우수한 요약문이 될 것입니다.",
            correctedText = if (userSummary.length > 10) userSummary + " (교정본: 더욱 자연스러운 문체와 정합성 높은 논리로 다듬어진 상태입니다.)" else "지문의 핵심 개념인 ${passage.title}의 관계를 강화하여 서술하는 것이 좋습니다.",
            aiSummary = passage.modelSummary,
            chatHistoryJson = gson.toJson(
                listOf(
                    ChatMessage("model", "안녕하세요! 작성하신 요약에 대한 분석 결과입니다. 피드백 내용 중 더 궁금하신 점이 있다면 질문해주세요. (현재 데모 모드로 동작 중입니다.)")
                )
            ),
            timestamp = System.currentTimeMillis(),
            passageType = determinePassageType(passage.title, passage.content)
        )
    }

    fun determinePassageType(title: String, content: String): String {
        val litKeywords = listOf(
            "소나기", "자전거", "도둑", "동백꽃", "진달래꽃", "시", "소설", "심청전", "홍길동전",
            "꺼삐딴", "태평천하", "탈춤", "가면극", "춘향전", "판소리", "관동별곡", "메밀꽃", "광장", "난장이가"
        )
        val isLit = litKeywords.any { title.contains(it) || content.contains(it) }
        return if (isLit) "문학" else "비문학"
    }
