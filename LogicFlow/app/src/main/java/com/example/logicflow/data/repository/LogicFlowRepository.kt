    // ── 429/503 재시도 헬퍼: 최대 maxRetries회, 지수 백오프 ──────────────
    private suspend fun <T> callWithRetry(
        maxRetries: Int = 3,
        initialDelayMs: Long = 3000L,
        block: suspend () -> T
    ): T {
        var lastException: Exception? = null
        repeat(maxRetries) { attempt ->
            try {
                return block()
            } catch (e: HttpException) {
                // 429(Rate Limit), 503(Service Unavailable) 만 재시도
                if (e.code() == 429 || e.code() == 503) {
                    lastException = e
                    val waitMs = initialDelayMs * (1L shl attempt) // 3s, 6s, 12s
                    delay(waitMs)
                } else {
                    throw e
                }
            }
        }
        throw lastException ?: Exception("알 수 없는 오류")
    }

